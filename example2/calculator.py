def add(a,b):
	c=a+b
	print(c)

def subtract(d,e):
    f=d-e
    print(f)	

def multiply(g,h):
    i=g*h
    print(i)

def divide(k,l):
    m=k/l
    print(m) 

def modulus(n,p):
    q=n%p
    print(q)  

def whole_division(r,t):
    v=r//t
    print(v)

def percentage(x,y):
	z=(x/y)*100
	print("{}%".format(z))

def exponent(s,u):
    w=s**u
    print(w)	
